
#整數除法(除法的商只保留整數部分，等同於無條件捨棄)
print(3//2) 

#數字轉字串
print(3+2)
print(str(3)+str(2))

#取絕對值
print(abs(-273.15))

#取指數
print(pow(2, 10))

#找最大值、最小值
print(max(-1, 0, 1))
print(min(-1, 0, 1))

#四捨五入
print(round(1.23456789000000000999999999))

#以下皆為引用函式庫
from math import *

#無條件捨去
print(floor(1.999999999))

#無條件進位
print(ceil(1.000000000000001))
print(ceil(1.0000000000000001)) #系統判定0.0000000000000001過小，不會無條件進位

#開平方根
print(sqrt(1048576))

